 # Handle incoming DMs to create a modmail thread
async def handle_modmail_message(bot, message):
    if isinstance(message.channel, discord.DMChannel):
        guild = bot.get_guild(GUILD_ID)
        modmail_channel = guild.get_channel(MODMAIL_CHANNEL_ID)

        if not modmail_channel:
            await message.author.send("Modmail is not set up properly. Please inform an admin!")
            return

        # Create a thread for the modmail
        thread = await modmail_channel.create_thread(
            name=f"Modmail from {message.author.name}",
            type=discord.ChannelType.private_thread,
            auto_archive_duration=1440
        )

        # Add allowed roles to the thread
        for role_id in ALLOWED_ROLE_ID:
            role = guild.get_role(role_id)
            if role:
                await thread.add_user(role)

        # Forward the user's message to the thread
        await thread.send(f"üì© **New Modmail from {message.author}**\n\n{message.content}")
        await message.author.send("Your message has been received! A moderator will reply to you soon.")
    elif isinstance(message.channel, discord.Thread) and message.channel.parent_id == MODMAIL_CHANNEL_ID:
        # Identify the user from the thread name
        thread_name = message.channel.name
        if not thread_name.startswith("Modmail from "):
            return

        # Extract the user's name
        user_name = thread_name.replace("Modmail from ", "")
        user = discord.utils.get(bot.users, name=user_name)

        if user:
            # Forward the moderator's reply to the user
            await user.send(f"üì© **Reply from the Moderation Team:**\n\n{message.content}")
            await message.channel.send(f"‚úÖ Your message was sent to {user}.")
        else:
            await message.channel.send("‚ùå Could not find the user to send the message.")

# Slash command to close a modmail thread
async def close_modmail(interaction: discord.Interaction):
    if isinstance(interaction.channel, discord.Thread) and interaction.channel.parent_id == MODMAIL_CHANNEL_ID:
        await interaction.channel.delete()
        await interaction.response.send_message("‚úÖ Modmail thread closed.", ephemeral=True)
    else:
        await interaction.response.send_message("‚ùå This command can only be used in modmail threads.", ephemeral=True)

# Integrate modmail functionality into an existing bot
def setup_modmail(bot):
    @bot.event
    async def on_message(message):
        # Pass the message to the modmail handler
        await handle_modmail_message(bot, message)

    # Add the close command to the bot
    @bot.tree.command(name="close", description="Close the current modmail thread")
    async def close_command(interaction: discord.Interaction):
        await close_modmail(interaction)

