class WYRView(View):
    def __init__(self, question, interaction):
        super().__init__(timeout=None)
        self.question = question
        self.interaction = interaction
        self.response = None

    @discord.ui.button(label="Yes", style=discord.ButtonStyle.green)
    async def yes_button(self, button: discord.ui.Button, interaction: discord.Interaction):
        await self.ask_for_reason(interaction, "Yes")

    @discord.ui.button(label="No", style=discord.ButtonStyle.red)
    async def no_button(self, button: discord.ui.Button, interaction: discord.Interaction):
        await self.ask_for_reason(interaction, "No")

    async def ask_for_reason(self, interaction: discord.Interaction, answer):
        # Defer the interaction to avoid "interaction already responded" error
        await interaction.response.defer()
        await interaction.followup.send(f"You chose {answer}. Why? Please explain.")

        # Wait for the user's response
        def check(msg):
            return msg.author == interaction.user and msg.channel == interaction.channel

        try:
            reason_msg = await bot.wait_for("message", check=check, timeout=60.0)
            reason = reason_msg.content
            await interaction.channel.send(f"Thanks for your response! You said: {reason}")
        except TimeoutError:
            await interaction.channel.send("You didn't respond in time! Let's move on.")

        # Ask if they want to play again
        await self.ask_to_play_again(interaction)

    async def ask_to_play_again(self, interaction: discord.Interaction):
        # Create a new view with the play-again button
        view = View()
        play_again_button = Button(label="Yes, play again", style=discord.ButtonStyle.green)

        async def play_again(interaction: discord.Interaction):
            new_question = random.choice(wyr_questions)
            await interaction.response.send_message(f"Here is a new question: {new_question}", view=WYRView(new_question, interaction))

        play_again_button.callback = play_again
        view.add_item(play_again_button)
        await interaction.followup.send("Do you want to play again?", view=view)

