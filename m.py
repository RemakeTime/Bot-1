#Command /resolved
@bot.tree.command(name="resolved", description="Mark the thread as resolved and schedule closure.")
async def resolved(interaction: discord.Interaction):
    thread = interaction.channel

    if isinstance(thread, discord.Thread):
        now = datetime.utcnow()
        close_time = now + timedelta(hours=1)
        closing_threads[thread.id] = close_time

        try:
            forum_channel = thread.parent
            resolved_tag = discord.utils.get(forum_channel.available_tags, id=RESOLVED_TAG_ID)
            unresolved_tag = discord.utils.get(forum_channel.available_tags, id=TAG_ID)

            # Add resolved tag and remove unresolved tag using their IDs
            if resolved_tag:
                await thread.add_tags([RESOLVED_TAG_ID])  # Use ID inside a list
                print(f"Tag '{resolved_tag.name}' added to thread '{thread.name}'")
            if unresolved_tag:
                await thread.remove_tags([TAG_ID])  # Use ID inside a list
                print(f"Tag '{unresolved_tag.name}' removed from thread '{thread.name}'")

            # Embed message
            embed = discord.Embed(
                title="Thread Marked as Resolved",
                description="This thread has been marked as resolved and will close in 1 hour. Use `/cancel` to stop closure.",
                color=0x00FF00,
                timestamp=now
            )
            embed.add_field(name="Closure Time", value="1 hour from now", inline=False)
            await interaction.response.send_message(embed=embed)

            # Wait 1 hour to close the thread, unless canceled
            await asyncio.sleep(3600)
            if thread.id in closing_threads and closing_threads[thread.id] == close_time:
                await thread.edit(locked=True, archived=True)
                del closing_threads[thread.id]
                print(f"Thread '{thread.name}' closed.")
        except Exception as e:
            await interaction.response.send_message(f"Error: {e}", ephemeral=True)
            print(f"Error in resolved command: {e}")
    else:
        await interaction.response.send_message("This command can only be used in a thread.", ephemeral=True)

# Command: /Cancel
@bot.tree.command(name="cancel", description="Cancel the scheduled thread closure.")
async def cancel(interaction: discord.Interaction):
    thread = interaction.channel

    if isinstance(thread, discord.Thread):
        if thread.id in closing_threads:
            del closing_threads[thread.id]
            embed = discord.Embed(
                title="Thread Closure Canceled",
                description="The automatic closure of this thread has been canceled.",
                color=0xFF0000,
                timestamp=datetime.utcnow()
            )
            await interaction.response.send_message(embed=embed)
        else:
            await interaction.response.send_message("This thread is not scheduled for closure.", ephemeral=True)
    else:
        await interaction.response.send_message("This command can only be used in a thread.", ephemeral=True)












